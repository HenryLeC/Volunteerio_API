function ValidateEmail(mail) 
{
    if (/^\w+([\.-]?\w+)*@\w+([\.-]?\w+)*(\.\w{2,3})+$/.test(mail))
    {
        return (true)
    }
    else
    {
        return (false)
    }
}

function ValidateContact()
{
    if (document.getElementById("contactName") != "" && ValidateEmail(document.getElementById("contactEmail").value) && document.getElementById("contactMessage").value != ""){
        return true;
    }
    else{
        alert("Please enter a valid email, name and message.");
        return false;
    }
}